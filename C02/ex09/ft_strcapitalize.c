char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		if ((*str >= 97 && *str <= 122))
		{
			*str -= 32;
		}
		while ((*str >= 48 && *str <= 57)
			|| (*str >= 65 && *str <= 90)
			|| (*str >= 97 && *str <= 122))
		{
			str++;
			i++;
			if ((*str >= 65 && *str <= 90))
			{
				*str += 32;
			}
		}
		str++;
		i++;
	}
	return (str - i);
}
